// The method used here to simulate the original behavior of the WASD CODE v2
// keyboard is as follows:
//
// The base layer corresponds to the keyboard with all DIP switches in the OFF
// position:
// - QWERTY
// - PC modifier order
// - GUI keys enabled
// - ScrollLock acts as ScrollLock
// - CapsLock acts as CapsLock
// - Menu key acts as Menu
//
// DIP switches 2:1 handle both key order and modifier order in a sort of
// confusing combination. We implement them as follows:
//
// - 1 off, 2 off: layer 0
// - 1 ON, 2 off: layer 0 + layer 1 (Mac modifier order)
// - 1 off, 2 ON: layer 0 + layer 2 (Dvorak key order)
// - 1 ON, 2 ON: layer 0 + layer 3 (Colemak key order)
//
// DIP switch 3 makes Capslock into a second LCTL when on:
// - 3 ON: adds layer 4 (move Caps)
//
// DIP switch 4 makes Scrolllock into a winkey-enable toggle:
// - 4 ON: adds layer 5 (remap scroll)
//
// (Note that, unlike the original WASD CODE firmware, if you enable this toggle
// and disable the GUI keys by default using dip switch 5, the toggle will still
// work.)
//
// DIP switch 5 disables the GUI keys:
// - 5 ON: adds layer 6 (GUI keys off)
//
// DIP switch 6 enables a Fn key:
// - 6 ON: adds layer 7 (Menu remapped as Fn)
//
// When the Fn key is enabled, it temporarily enables layer 8 when held. Layer 8
// provides basic media keys and backlight control. This layout extends the
// original Fn-key behavior on the CODE to add Fn+Esc for entering the
// bootloader.

#include QMK_KEYBOARD_H

bool dip_switch_update_mask_user(uint32_t state) {
    switch (state & 0b11) {
        case 0b00: // QWERTY basic
            layer_off(1);
            layer_off(2);
            layer_off(3);
            break;
        case 0b01: // QWERTY Mac
            layer_on(1);
            layer_off(2);
            layer_off(3);
            break;
        case 0b10: // DVORAK
            layer_off(1);
            layer_on(2);
            layer_off(3);
            break;
        case 0b11: // Colemak
            layer_off(1);
            layer_off(2);
            layer_on(3);
            break;
    }
    if (state & (1 << 2)) {
        layer_on(4);
    } else {
        layer_off(4);
    }
    if (state & (1 << 3)) {
        layer_on(5);
    } else {
        layer_off(5);
    }
    if (state & (1 << 4)) {
        layer_on(6);
    } else {
        layer_off(6);
    }
    if (state & (1 << 5)) {
        layer_on(7);
    } else {
        layer_off(7);
    }
    return true;
}

